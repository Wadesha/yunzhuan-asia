def letterize(opts):
    """给选项加字母前缀，返回 (options字符串, 答案字母映射)。"""
    return " | ".join("%s %s" % (chr(65 + i), o) for i, o in enumerate(opts))

def build_objective():
    qs = []
    pool = [f[2] for f in FACTS]  # 正确表述集合
    for subj, term, correct in FACTS:
        others = [p for p in pool if p != correct]
        random.shuffle(others)
        ds = others[:3]
        # 正向：下列说法正确的是
        opts = [correct] + ds
        random.shuffle(opts)
        ans = chr(65 + opts.index(correct))
        qs.append({"t": "single",
                   "q": "关于《%s》，下列说法正确的是？" % term,
                   "options": letterize(opts),
                   "a": ans,
                   "e": "考点：%s —— %s" % (term, correct)})
        # 反向：下列说法错误的是（答案为某个错误表述）
        wrong = ds[0]
        opts2 = [correct] + [wrong] + ds[1:3]
        random.shuffle(opts2)
        ans2 = chr(65 + opts2.index(wrong))
        qs.append({"t": "single",
                   "q": "关于《%s》，下列说法错误的是？" % term,
                   "options": letterize(opts2),
                   "a": ans2,
                   "e": "考点：%s —— 正确表述为：%s" % (term, correct)})
    return qs

