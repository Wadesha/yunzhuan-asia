PHRASES = [
    lambda t: "关于「%s」，下列说法正确的是？" % t,
    lambda t: "下列关于「%s」的表述，正确的是？" % t,
    lambda t: "下列有关「%s」的说法中，正确的是？" % t,
]

def mk_single(term, correct_def, pool_defs, idx):
    others = [d for d in pool_defs if d != correct_def]
    random.shuffle(others)
    distract = others[:3]
    while len(distract) < 3:
        distract.append(others[0] if others else correct_def)
    items = [(correct_def, True)] + [(d, False) for d in distract]
    random.shuffle(items)
    opts, ans = [], None
    for i, (txt, isc) in enumerate(items):
        L = chr(65 + i)
        opts.append("%s %s" % (L, txt))
        if isc:
            ans = L
    e = "考点：%s——%s" % (term, correct_def)
    return {"t": "single", "q": PHRASES[idx % len(PHRASES)](term),
            "options": "|".join(opts), "a": ans, "e": e}

def expand_list(name, arr, facts, target):
    if len(arr) >= target or not facts:
        return arr, 0
    defs = [d for (_, d) in facts]
    random.seed(hash(name) & 0xffffffff)
    added, i = 0, 0
    while len(arr) < target and i < target * 3:
        term, d = facts[i % len(facts)]
        q = mk_single(term, d, defs, i)
        arr.append(q)
        added += 1
        i += 1
    return arr, added

def main():
    total_added = 0
    for fn in sorted(os.listdir(B)):
        if not fn.endswith(".json") or not fn[:-5].isalpha() or fn.startswith("_"):
            continue
        if fn in ("_base.json", "_index.json"):
            continue
        pid = fn[:-5]
        path = os.path.join(B, fn)
        data = json.load(open(path, "r", encoding="utf-8"))
        if isinstance(data, list):
            # 单科 bank
            cur = len(data)
            if cur >= SKIP_TOTAL:
                continue
            data, ad = expand_list(pid, data, FACTS.get(pid, []), TARGET_FLAT)
            if ad:
                json.dump(data, open(path, "w", encoding="utf-8"), ensure_ascii=False, indent=2)
                print("%-14s +%3d -> %d" % (pid, ad, len(data)))
                total_added += ad
        elif isinstance(data, dict) and "sub" in data:
            # 分科 bank
            changed = False
            for s in data["sub"]:
                sid = s.get("id")
                qs = s.get("questions", [])
                if pid in SUBFACTS and sid in SUBFACTS[pid]:
                    facts = SUBFACTS[pid][sid]
                else:
                    facts = []
                if len(qs) >= TARGET_SUB or not facts:
                    continue
                s["questions"], ad = expand_list("%s/%s" % (pid, sid), qs, facts, TARGET_SUB)
                if ad:
                    changed = True
                    total_added += ad
                    print("%-14s sub=%s +%3d -> %d" % (pid, sid, ad, len(s["questions"])))
            if changed:
                json.dump(data, open(path, "w", encoding="utf-8"), ensure_ascii=False, indent=2)
    print("TOTAL ADDED:", total_added)

